package msg;

public class ImgId {
    private  String  imgId;

    public String getImgId() {
        return imgId;
    }

    public void setImgId(String imgId) {
        this.imgId = imgId;
    }

    @Override
    public String toString() {
        return "ImgId{" +
                "imgId='" + imgId + '\'' +
                '}';
    }
}
